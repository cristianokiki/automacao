    private void botoes(boolean adicionar, boolean alterar, boolean excluir, boolean lista) {
        this.jBTAdicionar.setEnabled(adicionar);
        this.jBTAlterar.setEnabled(alterar);
        this.jBTExcluir.setEnabled(excluir);
        this.jBTListar.setEnabled(lista);
    }

    private void preencheTab(String data) {
        List lista = VENDACNTRL.buscaListaTabela(ValidacaoCntrl.formataData(data));
        String[] coluna = {"CÓDIGO", "PRODUTO", "QUANTIDADE", "DATA", "HORA"};
        Tabela tab = new Tabela(lista, coluna);
        this.jTBVenda.setModel(tab);
    }

    private void AlterarVenda() {
        int linha = this.jTBVenda.getSelectedRow();
        String codigo = this.jTBVenda.getValueAt(linha, 0).toString();
        String quantidade = this.jTBVenda.getValueAt(linha, 2).toString();
        AlterarVendaDialogView dialog = new AlterarVendaDialogView(new javax.swing.JFrame(), true);
        dialog.setCodigo(codigo);
        dialog.setQuantidade(quantidade);
        dialog.setVisible(true);
    }
