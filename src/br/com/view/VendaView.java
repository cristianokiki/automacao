    private void jBTBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTBuscarActionPerformed
        if (!this.jFTFData.getText().equals("  -  -    ")) {
            this.preencheTab(this.jFTFData.getText());
        } else {
            this.preencheTab(null);
        }
    }//GEN-LAST:event_jBTBuscarActionPerformed

    private void jTBVendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTBVendaMouseClicked
        if (evt.getClickCount() == 1) {
            this.botoes(true, true, true, true);
        }
        if (evt.getClickCount() == 2) {
            AlterarVenda();
        }
    }//GEN-LAST:event_jTBVendaMouseClicked

