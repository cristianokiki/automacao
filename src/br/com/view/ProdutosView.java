    private void preencheTab(String categoria, Integer capacidade) {
        ProdutoCntrl prodCntrl = new ProdutoCntrl();
        List lista = prodCntrl.listarBuscaTabela(categoria, capacidade);
        String[] coluna = {"CODIGO", "CATEGORIA", "CAPACIDADE", "COR"};
        Tabela tab = new Tabela(lista, coluna);
        this.jTBProduto.setModel(tab);
    }

    private void botao(boolean adicionar, boolean alterar, boolean excluir, boolean listar) {
        this.jBTAdicionar.setEnabled(adicionar);
        this.jBTAlterar.setEnabled(alterar);
        this.jBTExcluir.setEnabled(excluir);
        this.jBTListar.setEnabled(listar);
    }

    private Integer pegaCodProduto() {
        int linha = this.jTBProduto.getSelectedRow();
        Integer prod_id = Integer.parseInt(this.jTBProduto.getValueAt(linha, 0).toString());
        return prod_id;
    }

    private void setAlterarProduto() {
        AlterarProdutoDialogView dialog = new AlterarProdutoDialogView(new javax.swing.JFrame(), true);
        dialog.setCodigoProduto(pegaCodProduto().toString());
        dialog.setVisible(true);
    }
