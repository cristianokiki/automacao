    private void jBTBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTBuscarActionPerformed
        String categoria = this.jTFCategoria.getText();
        String cap = this.jFTFCapacidade.getText().trim();
        Integer capacidade = null;
        try {
            if (cap != null && !cap.isEmpty()) {
                capacidade = Integer.parseInt(cap);
            }
            this.preencheTab(categoria, capacidade);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Formato de número inválido!\n" + ex.getMessage());
        }

    }//GEN-LAST:event_jBTBuscarActionPerformed

    private void jBTExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTExcluirActionPerformed
        int op = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o produto selecionado?");
        if (op == 0) {
            ProdutoCntrl prodCntrl = new ProdutoCntrl();
            try {
                if (prodCntrl.apagar(pegaCodProduto())) {
                    JOptionPane.showMessageDialog(null, "Excluído com sucesso!");
                }
            } catch (GenericSqlException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jBTExcluirActionPerformed


